"""Filterbench loader.

Filterbench is our synthetic multi-hop question set generated by
`synth/gold_path_generation.py`. Each row has `question`, `golden_answer`, and
provenance metadata (ideal_path, hop_count, filter_count).

The grader from `bench.browsecomp` works unchanged — `golden_answer` is passed
as `correct_answer`, and the judge compares against the extractor's final
answer the same way.
"""

from __future__ import annotations

import json
from pathlib import Path

from core.types import Task

_REPO_ROOT = Path(__file__).resolve().parent.parent
DATA_DIR = _REPO_ROOT / "bench" / "filterbench"


def _split_path(split: str) -> Path:
    return DATA_DIR / f"{split}.jsonl"


def load_tasks(split: str = "test", indices: list[int] | None = None) -> list[Task]:
    """Load filterbench questions from bench/filterbench/<split>.jsonl.

    `indices` filters by row position (0-based). If the JSONL is empty or
    missing, returns [].
    """
    path = _split_path(split)
    if not path.exists():
        raise FileNotFoundError(f"Filterbench split missing: {path}")

    tasks: list[Task] = []
    with open(path, encoding="utf-8") as f:
        for row_idx, line in enumerate(f):
            line = line.strip()
            if not line:
                continue
            row = json.loads(line)
            meta = {k: v for k, v in row.items() if k not in {"question", "golden_answer"}}
            tasks.append(
                Task(
                    idx=row_idx,
                    question=row["question"],
                    answer=row["golden_answer"],
                    metadata=meta,
                )
            )

    if indices is not None:
        keep = set(indices)
        tasks = [t for t in tasks if t.idx in keep]
    return tasks
